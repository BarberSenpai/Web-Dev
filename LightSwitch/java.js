function turnOn(){
	document.body.style.backgroundColor = "white";
	document.getElementById("on").style.backgroundColor = "gray";
	document.getElementById("off").style.backgroundColor = "white";
	
}

function turnOff(){
	document.body.style.backgroundColor = "black";
	document.getElementById("off").style.backgroundColor = "gray";
	document.getElementById("on").style.backgroundColor = "white";

}